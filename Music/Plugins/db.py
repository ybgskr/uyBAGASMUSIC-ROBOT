import asyncio

from Music import SUDOERS, app
from Music.MusicUtilities.database.chats import get_served_chats
from pyrogram import filters


@app.on_message(filters.command("broadcast") & filters.user(SUDOERS))
async def broadcast_message(_, message):
    if not message.reply_to_message:
        pass
    else:
        x = message.reply_to_message.message_id
        y = message.chat.id
        sent = 0
        pin = 0
        chats = []
        schats = await get_served_chats()
        for chat in schats:
            chats.append(int(chat["chat_id"]))
        for i in chats:
            try:
                m = await app.forward_messages(i, y, x)
                try:
                    await m.pin(disable_notification=False)
                    pin += 1
                except Exception:
                    pass
                await asyncio.sleep(0.3)
                sent += 1
            except Exception:
                pass
        await message.reply_text(
            f"**Pesan Siaran Dalam Obrolan {sent} dengan {pin} Pin.**"
        )
        return
    if len(message.command) < 2:
        await message.reply_text("**Penggunaan**:\n/broadcast [MESSAGE]")
        return
    text = message.text.split(None, 1)[1]
    sent = 0
    pin = 0
    chats = []
    schats = await get_served_chats()
    for chat in schats:
        chats.append(int(chat["chat_id"]))
    for i in chats:
        try:
            m = await app.send_message(i, text=text)
            try:
                await m.pin(disable_notification=False)
                pin += 1
            except Exception:
                pass
            await asyncio.sleep(0.3)
            sent += 1
        except Exception:
            pass
    await message.reply_text(
        f"**Pesan yang Disiarkan Dalam Obrolan {sent} dan Pin {pin}.**"
    )
